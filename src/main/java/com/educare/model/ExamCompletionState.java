package com.educare.model;

public class ExamCompletionState {
	private String examname;
	private String examn_status;
	private String exendtime;
	private String exenddate;
	
	

	public String getExendtime() {
		return exendtime;
	}

	public void setExendtime(String exendtime) {
		this.exendtime = exendtime;
	}

	public String getExenddate() {
		return exenddate;
	}

	public void setExenddate(String exenddate) {
		this.exenddate = exenddate;
	}

	public String getExamname() {
		return examname;
	}

	public void setExamname(String examname) {
		this.examname = examname;
	}

	public String getExamn_status() {
		return examn_status;
	}

	public void setExamn_status(String examn_status) {
		this.examn_status = examn_status;
	}

}
